package third;

public class switch简要说明 {
    /** switch语法：
     *
     *
            switch(表达式){


        case 常量表达式1:
            语句块1
            break;

        case 常量表达式2:
            语句块2
            break;

        case 常量表达式3:
            语句块3
            break;
                 ………………

        default:
            语句块n
            break;
     */


    /**  语义：先得出表达式的结果，根据结果跳转到相应的case语句块里面去执行

     表达式结果  == 常量表达式  就叫相应的case语句块

     如果case 后面的常量表达式没有一个跟表达式结果对应的
     *则跳转到default执行
     */
}


